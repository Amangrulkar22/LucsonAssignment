import Foundation
import UIKit

/**
 Set border color for uitextfield, uibutton, uiview using runtime entities
 */
public extension CALayer {
    var borderUIColor: UIColor {
        set {
            self.borderColor = newValue.cgColor
        }
        
        get {
            return UIColor(cgColor: self.borderColor!)
        }
    }
}
